#include "../../inc/server/Utils.hpp"

void    RemoveTab(std::string &key)
{
    int i = 0;
    int j = 0;
    while (key[j])
    {
        if (key[i] == '\t')
            i++;
        j++;
    }
    key.erase(0, i);
}